            <div style="text-align: justify;">Se denomina <strong>contaminación ambiental</strong> a la presencia de componentes nocivos (ya sean químicos, físicos o biológicos) en el medio ambiente (entorno natural y artificial), que supongan un perjuicio para los seres vivos que lo habitan, incluyendo a los seres humanos. La contaminación ambiental está originada principalmente por causas derivadas de la actividad humana, como la emisión a la atmósfera de gases de efecto invernadero o la explotación desmedida de los recursos naturales. </p>
<h2>Causas de la contaminación ambiental</h2>
<p>Las fuentes antropogénicas que generan mayor contaminación ambiental son las siguientes: </p>


<ul>
<li>Tala excesiva de árboles.</li>
<li>Emisiones y vertidos industriales a la atmósfera y a la hidrosfera.</li>
<li>Extracción, procesamiento y refinamiento de combustibles fósiles (petróleo, carbón y gas natural).</li>
<li>Producción de energía con combustibles fósiles y otras fuentes no renovables.</li>
<li>Uso excesivo de automóviles y otros medios de transporte impulsados por gasolina o diésel.</li>
<li>Uso indiscriminado de plásticos y otros materiales derivados del petróleo.</li>
<li>Liberación de plásticos y objetos no biodegradables en espacios naturales.</li>
</ul>
<p></p>
<h2>Consecuencias de la contaminación ambiental</h2>
<p>Una de las principales consecuencias de la contaminación ambiental es el <strong> calentamiento global </strong>, también conocido como <em>cambio climático</em>, por el cual la temperatura del planeta va aumentando de manera progresiva, tanto la temperatura atmosférica como la de mares y océanos.</p>
<p>La contaminación ambiental supone un riesgo para la salud de los seres vivos que habitan los ecosistemas contaminados, incluyendo a los seres humanos. Además, la tala indiscriminada, la explotación excesiva de los recursos naturales y la emisión de contaminantes al medio ambiente (gases a la atmósfera, vertidos en medios acuáticos, residuos sólidos) provoca la <strong>destrucción de ecosistemas</strong>. De esta forma, muchas especies de animales y plantas ven cómo su hábitat natural se va reduciendo cada vez más, pudiendo llegar a provocar incluso su extinción.</div>

