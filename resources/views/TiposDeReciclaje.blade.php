                <p class="text-left mt-3 post-txt">
                    <span>Tipos de reciclaje</span>
                    <span class="float-right">y en que consisten</span>
                </p>
                <p class="text-left">
                El reciclaje es un factor de gran importancia para la conservación de nuestro medio ambiente, el cual nos aporta una serie de beneficios, como por ejemplo: permite ahorrar energía, evita la explotación de los recursos naturales, reduce la contaminación, permite alargar la vida útil de los vertederos y un largo etc.
                Cuando los desechos generados se procesan y se transforman, podemos obtener materias primas, las cuales serán utilizadas en nuevos procesos de fabricación. De esta forma, el reciclaje contribuye a eliminar los residuos generados, a ahorrar en los procesos industriales y a reducir la explotación de los recursos naturales.
                Para todo ello, existen diferentes tipos de reciclaje que vamos a ver a continuación.
                </p>

                <h3>Reciclaje Mecàico</h3>
                <p>Se refiere a todas las técnicas y procesos que incluyan el trabajo manual o ayudado por máquinas. Este tipo de reciclaje es utilizado mucho en la clasificación de residuos mezclados y en el reciclaje del plástico.</p>
                <h3>Reciclaje químico</h3>
<p>Incluye técnicas y procedimientos que implican el cambio en la estructura química del material. Su finalidad es la descomposición del polímero para la obtención de monómeros. Este tipo de reciclaje es utilizado mediante disoluciones, hidrólisis, hidrogenación;</p>
<h3>Reciclaje energético</h3>
<p>Se utiliza este tipo de reciclaje en materiales cuyo fin es el aprovechamiento energético y cuyos residuos no pueden ser clasificados y recuperado, por lo que, debido a esta imposibilidad técnica o económica, se aprovechan de este modo. Un ejemplo de estos son la incineración de residuos, pirólisis y gasificación.</p>
<h3>Reciclaje por residuo</h3>
<p>En este tipo de reciclaje, lo que se busca es la degradación de toda la materia orgánica en presencia o en ausencia de oxígeno. Estos tratamientos pueden emplearse con la materia orgánica separada en origen o con la materia orgánica sin separar, utilizando procesos mecánicos complementarios en este último caso.</p>
<h2>Tipos de reciclaje según el material de residuo</h2>
<p>Acabamos de ver cuales son los principales tipos de reciclajes que hay actualmente. A continuación, veremos qué tipo de reciclaje recibe cada tipo de material de residuo.</p>
<h3>Reciclaje de papel y carton</h3>
<p>Se recogen y se trasladan a planta, una vez allí, se mezclan con agua en una máquina llamada Pulper. Se utilizan aditivos para darle mayor resistencia y blancura y, finalmente, se retiran los impropios como las tintas, grapas etc.</p>
<p>Para terminar el proceso, se tira esa pasta resultante en un tapiz  en el cual se van secando y posteriormente produciendo.</p>
<h3>Reciclaje de plástico</h3>
<p>El plástico es sin duda uno de los residuos que más tardan en degradarse, llegando a tardar la naturaleza hasta cientos de años en degradarlos. El reciclaje de plástico se puede realizar de dos maneras:</p>

<li>Mecánica: Consiste en recoger el plástico, limpiarlo, molerlo o trocearlo, lavarlo de nuevo y enfardarlo.</li>
<li>Químico: Consiste en degradar el plástico para conseguir moléculas simples mediante métodos como:

<li>Pirólisis: Altas temperaturas en ausencia de oxígeno.</li>
<li>Hidrogenación: Altas temperaturas en presencia de hidrógeno.</li>
<li>Gasificación: Convierte el plástico en combustible en presencia de oxígeno o hidrógeno.</li>
<li>Craqueo: Altas temperaturas y catalizador.</li>
</li>
</ul>
<h3>Reciclaje de vidrio</h3>
<p>Una de las principales ventajas del vidrio es que puede ser reciclado y reutilizado tantas veces como sea necesario dado que, no pierde características ni propiedades.</p>
<ul>
<li>Cuando llega a las fábricas, se separa según el color.</li>
<li>Después, se separa de todo el material impropio, como las etiquetas y los tapones.</li>
<li>Por último, se tritua y se convierte en polvo (calcín), se somete a altas temperaturas y se le añade arena, hidróxido de sodio y caliza.</li>
</ul>
<h3>Reciclaje del textil y calzado</h3>
<p>La ropa que es recogida y que no puede ser rehutilizada, es deshilada para formar otros productos como mantas, trapos para uso industriales&#8230; Si esto no es posible, se incinera para recuperar energía o se lleva al vertedero.</p>
<h3>Reciclaje de residuos orgánicos</h3>
<p>También denominados biorresiduos. Está formado por todos los residuos biodegradables que podemos encontrar en el sector doméstico y comercial, pudiendo ser de origen vegetal o animal. Tanto si el tratamiento es anaeróbico como aeróbico, una población microbiana utiliza esta materia como alimento, degradándola y convirtiéndola.</p>
<h3>Reciclaje de pilas y baterías</h3>
<p>Son potencialmente peligrosos y muy contaminantes debido a la presencia de metales pesado. Mediante un proceso mecánico se realiza una trituración refrigerada, a continuación, se lava con agua y pasa por una mesa vibratoria la cual se encarga de separar los metales férreos, no férreos, plástico, papel y polvo de pilas.</p>
<p>Este polvo tiene otro proceso hidrometalúrgico que con ayuda de otros reactivos consigue recuperar los diferentes componentes.</p>
<h3>Reciclaje de chatarra o metales</h3>
<p>Puede ser una mezcla, o no, de metales férricos (aquellos que contienen hierro) y no férricos (aquellos que no contienen hierro). El proceso de reciclaje es mecánico, mediante separación, cribados y corrientes de Foucault para separar los diferentes residuos, y, para finalizar, se trituran.</p>
<h3>Reciclaje de tierras y escombros</h3>
<p>Residuos procedentes de obras y edificaciones.</p>
<ul>
<li>Su reciclaje comienza con una primera separación de los materiales, donde se quitan los más voluminosos.</li>
<li>A continuación, pasa por un tromel que separa los residuos por tamaños.</li>
<li>El siguiente paso es quitar los pequeños residuos impropios utilizando chorros de aire para separar según su densidad.</li>
<li>Finalmente, pasa por un imán donde se separarán todos los materiales férricos del resto, para terminar, se tritura el residuo consiguiendo un material homogéneo.</li>
</ul>
<h3>Reciclaje de agua</h3>
<p>El agua también se recicla. Todo el agua que pasa por nuestros desagües sufre una serie de tratamientos físico-químicos y biológicos los cuales ayudarán a su limpieza.</p>
<ul>
<li>Para reciclar el agua, en primer lugar se retira los residuos sólidos y grandes que se encuentran mediante rejas y tamices eliminando plásticos, grasas, papeles etc.</li>
<li>El siguiente paso es tratar el agua con reactivos químicos para eliminar los sólidos que por su pequeño tamaño no ha podido eliminarse en la primera fase..</li>
<li>A continuación, se introducen bacterias en el agua para que conviertan la materia orgánica disuelta en ella en flóculos, los cuales pueden ser separados mediante cecantación.</li>
<li>En el penúltimo paso, se aplica un tratamiento químico para eliminar del agua los restos de fosdatos, virus y gérmenes.</li>
<li>Por último, se desinfecta el agua y se prepara para su uso y consumo.</li>
</ul>
<h3>Reciclaje de la basura</h3>
<p>Solemos denominar como basura todo residuo generado dentro del ámbito doméstico y comercial y que se tira sin separación previa. En este caso, es mas difícil clasificarla, dado que, depende de la estación del año, de si estamos en una población costera o no, si es turística, del nivel de vida etc.</p>

