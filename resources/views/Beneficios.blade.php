            <ul>
<li style="list-style-type: none">
<ul>
<li>Permite <strong>ahorrar energía</strong> de forma significativa. Es menos costoso reciclar un material que fabricarlo desde cero, como ocurre por ejemplo con el vidrio.</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ul>
<li style="list-style-type: none">
<ul>
<li>Reciclar ayuda a <strong>evitar la explotación de los recursos naturales</strong>. El reciclaje hace posible que los materiales originales puedan ser aprovechados con un nuevo uso, sin que sea necesario volver a usar recursos naturales para fabricarlos.</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ul>
<li style="list-style-type: none">
<ul>
<li>Se evitan los métodos de extracción de recursos naturales, que son invasivos y contaminantes. El reciclaje permite no sustraer nueva materia prima para fabricación y se puede simplemente reciclar la materia existente.</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ul>
<li style="list-style-type: none">
<ul>
<li>Se <strong>reduce la contaminación</strong>, proporcionando una atmósfera más limpia. Reciclando ayudamos a reducir el daño producido al medio ambiente.</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ul>
<li style="list-style-type: none">
<ul>
<li>Se conserva el medio ambiente ya que permite <strong>reducir la cantidad de desechos sólidos que llegan a los vertederos</strong>. Esto hace posible que los vertederos ocupen menos espacio e incluso puedan llegar a cerrarse, evitando el impacto negativo que causan sobre el medio ambiente.</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ul>
<li style="list-style-type: none">
<ul>
<li>Permite alargar la vida útil de los vertederos, ya que se llenan a un menor ritmo evitando que se abran más vertederos.</li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<ul>
<li style="list-style-type: none">
<ul>
<li>Si depositamos los desechos que generamos en el contenedor correspondiente para que sean reciclados se reducen los costes de recolección y clasificación de los residuos, ya que son las personas las que ayudan a realizar dicha labor.</li>
</ul>
</li>
</ul>
